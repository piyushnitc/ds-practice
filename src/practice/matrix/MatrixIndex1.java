package practice.matrix;

public class MatrixIndex1 {
        int row1;
        int col1;

        public MatrixIndex1(int row1, int col1) {
            this.row1 = row1;
            this.col1 = col1;
        }

    public int getRow1() {
        return row1;
    }

    public int getCol1() {
        return col1;
    }
}
